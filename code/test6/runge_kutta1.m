function E=runge_kutta1(f,y0,h,a,b)%参数表顺序依次是微分方程组的函数名称，初始值向量，步长，时间起点，时间终点（参数形式参考了ode45函数）
n=floor((b-a)/h);%求步数
x(1)=a;%时间起点
y(:,1)=y0;%赋初值，可以是向量，但是要注意维数
    for t=1:n
        x(t+1)=x(t)+h;
        k1=f(x(t),y(:,t));
        k2=f(x(t)+h/2,y(:,t)+h*k1/2);
        k3=f(x(t)+h/2,y(:,t)+h*k2/2);
        k4=f(x(t)+h,y(:,t)+h*k3);
        y(:,t+1)=y(:,t)+h*(k1+2*k2+2*k3+k4)/6;
    %按照龙格库塔方法进行数值求解
    end
    E = [x' y'];
end